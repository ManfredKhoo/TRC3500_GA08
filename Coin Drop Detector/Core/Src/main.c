/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM2_Init();

  /* USER CODE BEGIN 2 */
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET);

  HAL_TIM_Base_Start(&htim2);
  HAL_ADC_Start_DMA(&hadc1, (uint32_t *)&adcLatest, 1);

  snprintf(msg, sizeof(msg),
           "READY baseline=%u threshold=%u nsamples=%u\r\n",
           BASELINE_ADC, SPIKE_THRESHOLD, NSAMPLES);
  HAL_UART_Transmit(&huart2, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
  /* USER CODE END 2 */

  while (1)
  {
    if (adcSampleReady)
    {
      adcSampleReady = 0;
      uint16_t sample = adcLatest;

      if (!capturing)
      {
        if (abs((int)sample - BASELINE_ADC) >= SPIKE_THRESHOLD)
        {
          capturing = 1;
          captureDone = 0;
          captureIndex = 0;

          HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_SET);

          captureBuf[captureIndex++] = sample;
        }
      }
      else
      {
        if (captureIndex < NSAMPLES)
        {
          captureBuf[captureIndex++] = sample;
        }

        if (captureIndex >= NSAMPLES)
        {
          capturing = 0;
          captureDone = 1;
          HAL_GPIO_WritePin(GPIOB, GPIO_PIN_3, GPIO_PIN_RESET);
        }
      }
    }

    if (captureDone)
    {
      captureDone = 0;
      SendCaptureBuffer();

      snprintf(msg, sizeof(msg), "READY\r\n");
      HAL_UART_Transmit(&huart2, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
    }
  }
}

