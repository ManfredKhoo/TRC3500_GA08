static void SendCaptureBuffer(void)
{
  snprintf(msg, sizeof(msg), "START\r\n");
  HAL_UART_Transmit(&huart2, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);

  for (uint16_t i = 0; i < NSAMPLES; i++)
  {
    int len = snprintf(msg, sizeof(msg), "%u\r\n", captureBuf[i]);
    HAL_UART_Transmit(&huart2, (uint8_t *)msg, len, HAL_MAX_DELAY);
  }

  snprintf(msg, sizeof(msg), "END,N=%u\r\n", NSAMPLES);
  HAL_UART_Transmit(&huart2, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
}
